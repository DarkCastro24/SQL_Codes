-- SCRIPT DE CREACION DE TABLAS
CREATE TABLE Categoria (
    CategoriaID INT PRIMARY KEY,
    Nombre NVARCHAR(50) NOT NULL UNIQUE,
    Descripcion NVARCHAR(200)
);

CREATE TABLE Producto (
    ProductoID INT PRIMARY KEY,
    CategoriaID INT NOT NULL REFERENCES Categoria(CategoriaID),
    Nombre NVARCHAR(100) NOT NULL,
    PrecioUnitario DECIMAL(10,2) NOT NULL CHECK (PrecioUnitario >= 1 AND PrecioUnitario <= 400)
);

CREATE TABLE Empleado (
    EmpleadoID INT PRIMARY KEY,
    Nombre NVARCHAR(50) NOT NULL,
    Apellido NVARCHAR(50) NOT NULL,
    FechaContratacion DATE NOT NULL,
    Salario DECIMAL(10,2) NOT NULL CHECK (Salario >= 400 AND Salario <= 3000)
);

CREATE TABLE Cliente (
    ClienteID INT PRIMARY KEY,
    Nombre NVARCHAR(50) NOT NULL,
    Apellido NVARCHAR(50) NOT NULL,
    Email NVARCHAR(100) UNIQUE,
    Telefono NVARCHAR(20)
);

CREATE TABLE MetodoPago (
    MetodoPagoID INT PRIMARY KEY,
    Nombre NVARCHAR(30) NOT NULL UNIQUE
);

CREATE TABLE Factura (
    FacturaID INT PRIMARY KEY,
    EmpleadoID INT NOT NULL REFERENCES Empleado(EmpleadoID),
    ClienteID INT NOT NULL REFERENCES Cliente(ClienteID),
    MetodoPagoID INT NOT NULL REFERENCES MetodoPago(MetodoPagoID),
    FechaFactura DATE NOT NULL,
    Monto DECIMAL(12,2) NOT NULL
);

CREATE TABLE DetalleFactura (
    DetalleID INT PRIMARY KEY,
    FacturaID INT NOT NULL REFERENCES Factura(FacturaID),
    ProductoID INT NOT NULL REFERENCES Producto(ProductoID),
    Cantidad INT NOT NULL CHECK (Cantidad > 0),
    PrecioUnitario DECIMAL(10,2) NOT NULL
);

CREATE TABLE Inventario (
    InventarioID INT PRIMARY KEY,
    ProductoID INT NOT NULL REFERENCES Producto(ProductoID) UNIQUE,
    Stock INT NOT NULL
);

